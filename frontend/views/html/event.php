                    <h3>Dawn or Doom: The effects of Brexit on immigration, wages, and employment</h3>
                    <p>A panel discussion with economist <strong>Jonathan Portes</strong> (National Institute of Economic and Social Research), Conservative politician <strong>Geoffrey Van Orden</strong> (Member of the European Parliament), <strong>Professor L. Alan Winters</strong> (Sussex University and IZA), <strong>Allie Renison</strong> (Head of Europe and Trade Policy at the Institute of Directors), and moderated by Economist journalist <strong>Philip Coggan</strong>.</p>
                    <p>Three days before the referendum on a Brexit <a href="">IZA World of Labor</a> and the Bloomsbury Institute are bringing together a high-profile panel to discuss the effects a possible Brexit might have on the UK labor market and EU immigration.</p>
                    <p>When it comes to evaluating the repercussions of Britain leaving the EU, claims are being made with vehemence; some are calling it “disastrous,” whilst others state that it will result in a “booming Britain.”  But, as so often the case, the answer is neither black nor white, and the issues are far more complex. To help demystify the implications of Brexit, particularly from a labor market perspective, IZA World of Labor is gathering a panel of experts to give us their views on this difficult question—in or out—before the nation goes to vote.</p>
                    <p>The discussion will take place in the evening of the 20th of June at the Bloomsbury Institute in London, and will be followed by a drinks reception.</p>
                    <h3>The Panel</h3>
                    <p><strong>Jonathan Portes</strong> is Principal Research Fellow at the National Institute of Economic and Social Research; and Senior Fellow, ESRC Britain in a Changing Europe. Previously, he was Chief Economist at the Cabinet Office, where he advised the Cabinet Secretary, Gus O’Donnell, and Number 10 Downing Street on economic and financial issues. Before that he held a number of other senior economic policy posts in the UK government. His particular interests include immigration, labor markets, and poverty. Mr Portes began his civil service career in HM Treasury in 1987.
                    <p><strong>Allie Renison</strong> is Head of Europe and Trade Policy at the Institute of Directors. She is devising recommendations and representing the voice of members on EU reform matters and helping to provide the link between business and government on increasing international trade. She also routinely provides advocacy for the IoD on a range of regulatory issues in Brussels. She was previously Research Director at Business for Britain, the campaign focused on renegotiating the UK's relationship with the EU. Prior to that, she advised a number of parliamentarians in both Houses on EU legislative issues, with a particular focus on trade and employment policy areas.</p>
                    <p><strong>Geoffrey Charles Van Orden</strong> MBE is a British politician and former Army officer. He is currently Member of the European Parliament for the East of England region for the Conservative Party. He was first elected to the European Parliament in 1999. He has consistently opposed European political integration and taken a tough stance on immigration. As Britain renegotiates its relationship with the EU ahead of an in-out referendum, he has asked for substantive change. Geoffrey sits on the Foreign Affairs Committee and Security and Defence Subcomittee.</p>
                    <p><strong>L. Alan Winters</strong> CB is Professor of Economics at the University of Sussex. He is a Research Fellow and former Programme Director of the Centre for Economic Policy Research (CEPR, London) and a contributor to
                        <a href="">IZA World of Labor</a>. He is CEO of the Migrating Out Of Poverty Research Consortium based in Sussex. From 2008 to 2011 he was Chief Economist at the British government’s Department for International Development (DFID), and from 2004 to 2007 Director of the Development Research Group of the World Bank, the world’s largest and leading group of development economists. He has previously worked as Division Chief and Research Manager (1994–99) and Economist (1983–85) in the World Bank and in the universities of Cambridge, Bristol, Bangor, and Birmingham.</p>
                    <p><strong>Moderator: Phil Coggan</strong> is the Buttonwood columnist of The Economist. Previously, he worked for the Financial Times for 20 years, most recently as Investment Editor. In that post, he founded the “Short View” column and wrote the “Long View” and “Last Word” columns. In 2009, he was voted Senior Financial Journalist of the Year in the Wincott awards and best communicator in the business journalist of the year awards. Among his books are The Money Machine, a guide to the city that is still in print after 25 years and The Economist Guide to Hedge Funds. His book Paper Promises; Money Debt and the New world Order was Spears’ business book of the year in 2012. His latest book, The Last Vote: The Threats to Western Democracy was published in September 2013.</p>
                    <h3>About the hosts</h3>
                    <p><strong>IZA World of Labor</strong> is a global, freely available online resource that provides policy makers, academics, journalists, and researchers, with clear, concise, and evidence-based knowledge on labor economics issues worldwide. The site offers relevant and succinct information written by internationally renowned experts on topics including diversity, migration, minimum wage, youth unemployment, employment protection, development, education, gender balance, labor mobility and flexibility, among others.</p>
                    <p><strong>The Bloomsbury Institute</strong> was launched in January 2012 and has hosted joint events with a wide variety of organizations including Book Aid, Granta, Intelligence Squared, Psychologies Magazine, and Waterstones. It is Bloomsbury Publishing’s very own programme of author events in the heart of literary London, hosted at our offices in the very Georgian Bedford Square.</p>
