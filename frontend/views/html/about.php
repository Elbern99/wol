			<h2>About IZA</h2>
			<p>The Institute for the Study of Labor (IZA) is a private independent nonprofit economic research institute that analyzes global labor markets. Central to IZA’s mission is making evidence-based scholarly analysis available globally. Established in 1998 in Bonn, Germany, IZA supports an international network of about 1,300 economists and researchers in more than 45 countries.</p>
			<br>
			<h2>About our partners</h2>
			<p>IZA World of Labor is an innovative resource developed by IZA in partnership with Bloomsbury Publishing Plc and supported by the World Bank. It relies on the financial support from the Deutsche Post Foundation.</p>
